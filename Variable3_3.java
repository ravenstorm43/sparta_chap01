public class Variable3_3 {
    public static void main(String[] args) {
        int num = 5;
        char ch = '5';

        System.out.println("num + \"\" = " + num + "");
        System.out.println("num + \"\" + \"5\" = " + num + "" + "5");

        System.out.println("ch + \"\" = " + ch + "");
        System.out.println("ch + \"\" + \"8\" = " + ch + "" + "8");
    }
}
